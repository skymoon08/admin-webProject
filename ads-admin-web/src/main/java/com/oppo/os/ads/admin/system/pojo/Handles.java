package com.oppo.os.ads.admin.system.pojo;

public class Handles {
    private String id;
    private String url;
    private String handle_name;
    private String sort;
    private String params;
    private String content;
    private String leftMenu_id;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getHandle_name() {
        return handle_name;
    }

    public void setHandle_name(String handle_name) {
        this.handle_name = handle_name;
    }

    public String getSort() {
        return sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public String getParams() {
        return params;
    }

    public void setParams(String params) {
        this.params = params;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getLeftMenu_id() {
        return leftMenu_id;
    }

    public void setLeftMenu_id(String leftMenu_id) {
        this.leftMenu_id = leftMenu_id;
    }
}
