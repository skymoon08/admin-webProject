package com.oppo.os.ads.admin.system.pojo;

public class TopTitle {
    private String id;
    private String type;
    private String toptitle_name;
    private String sort;
    private String url;
    private String icon;
    private LeftMenu leftMenu;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getToptitle_name() {
        return toptitle_name;
    }

    public void setToptitle_name(String toptitle_name) {
        this.toptitle_name = toptitle_name;
    }

    public String getSort() {
        return sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public LeftMenu getLeftMenu() {
        return leftMenu;
    }

    public void setLeftMenu(LeftMenu leftMenu) {
        this.leftMenu = leftMenu;
    }
}
